#include<bits/stdc++.h>
using namespace std;
typedef long long ll;

int a[10][10] = {
    {},
    {-1, 0, 2, 3, 4, 5, 6, 7, 1},
    {-1, 1, 2, 3, 4, 5, 6, 0, 7},
    {-1, 1, 2, 3, 4, 5, 6, 7, 0},
    {-1, 1, 2, 3, 4, 5, 6, 0, 7},
    {-1, 1, 2, 3, 4, 5, 6, 7, 0},
    {-1, 1, 2, 3, 4, 5, 6, 0, 7},
    {-1, 1, 2, 3, 4, 5, 6, 7, 0},
    {-1, 1, 2, 3, 4, 5, 6, 0, 7},
};
int r[105], c[105];

int main() {
    for(int i = 1; i <= 8; ++i) {
        for(int j = 1; j <= 8; ++j) {
            a[i][j] += (i - 1) * 8;
            int p = a[i][j];
            r[p] = i; c[p] = j;
        }
    }
    int n;
    cin >> n;
    if(n == 50) {
        r[47] = 7, c[47] = 6;
        r[48] = 7, c[48] = 7;
        r[49] = 7, c[49] = 8;
        r[50] = 8, c[50] = 8;
    } else if(n <= 57) {
        if(c[n-2] == 8) {
            r[n-1] = 7; c[n-1] = 8;
            r[n] = 8, c[n] = 8;
        } else {
            r[n-1] = 8; c[n-1] = c[n-2];
            r[n] = 8, c[n] = 8;
        }
    } else {
        r[n] = 8, c[n] = 8;
    }
    for(int i = 0; i <= n; ++i) {
        printf("%c%d ", c[i]-1+'a', r[i]);
    }
    printf("\n");

}